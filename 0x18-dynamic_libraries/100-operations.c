#include <stdio.h>

/**
 * add - function to add 2 numbers
 * @a: first number to add
 * @b: second number to add
 *
 * Return: the sum of the numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function to subtract 2 numbers
 * @a: minuend
 * @b: subtraend
 *
 * Return: the difference of the numbers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function to multiply 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: the product of the numbers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function for division
 * @a: dividend
 * @b: divisor
 *
 * Return: the quotient
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function for modulos
 * @a: dividend
 * @b: divisor
 *
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
